public class UserInfo {

    int id;
    String firstName;
    String lastName;
    String email;

    public UserInfo() {
    }

    public int getId() {
        return id;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getEmail() {
        return email;
    }
}
